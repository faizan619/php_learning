$favouriteColor = "pink";
switch ($favouriteColor) {
    case 'green':
        echo "Favourite Color  is green";
        break;
    case 'black':
        echo "Favourite color is black";
        break;
    case 'red':
        echo "Favourite Color is red";
        break;
    default:
        echo "Nahi Batana";
        break;
}
